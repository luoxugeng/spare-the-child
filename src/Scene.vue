<script>
    import echarts from "echarts";
    import 'echarts-wordcloud';
    const option = {
        tooltip: {
            show: false
        },
        series: [{
            type: 'wordCloud',
            gridSize: 1,
            sizeRange: [20, 100],
            rotationRange: [-45, 0, 45, 90],
            left: 'center',
            top: 'center',
            width: '100%',
            height: '100%',
            right: "100%",
            bottom: "100%",
            // width: 300,
            // height: 200,
            // top: 20,
            data: []
        }]
    }
    export default {
        name: "Scene",
        mounted () {
            this.getData()
            this.mycharts = echarts.init(this.$refs.mapbox)
            this.mycharts.setOption(option)
        },
        methods: {
            getData () {
                this.$http.get('static/case_scene.json').then(function(response){
                    let list = response.body.data.list.map(item => ({name: item.name, value: item.weight }))
                    option.series[0].data = list
                    this.mycharts.setOption(option)
                }).catch(function(response){
                    console.log(response)
                })
            }
        }
    }
</script>

